<?php

namespace app\controllers;

use app\models\usermodels\InviteForm;
use app\models\usermodels\LoginForm;
use app\models\usermodels\PasswordResetRequestForm;
use app\models\usermodels\ResendVerificationEmailForm;
use app\models\usermodels\ResetPasswordForm;
use app\models\usermodels\VerifyEmailForm;
use Yii;
use yii\base\InvalidArgumentException;
use yii\filters\AccessControl;
use yii\filters\VerbFilter;
use yii\web\BadRequestHttpException;
use yii\web\Controller;
use yii\web\ForbiddenHttpException;
use yii\web\Response;

class SiteController extends Controller
{
    /**
     * {@inheritdoc}
     */
    public function behaviors()
    {
        return [
            'access' => [
                'class' => AccessControl::class,
//                'only' => ['logout'],
                'rules' => [
                    [
                        'actions' => [
                            'signout',
                            'login',
                            'error',
                            'about',
                            'request-password-reset',
                            'reset-password',
                            'verify-email',
                            'resend-verification-email'],
                        'allow' => true,
                    ],
                    [
                        'actions' => ['index'], // add all actions to take guest to login page
                        'allow' => true,
                        'roles' => ['@'],
                    ],
                    [
                        'actions' => [
                            'invite'
                        ],
                        'allow' => true,
                        'roles' => ['userCreate']
                    ]
                ],
            ],
            'verbs' => [
                'class' => VerbFilter::class,
                'actions' => [
                    'logout' => ['post'],
                ],
            ],
        ];
    }

    /**
     * {@inheritdoc}
     */
    public function actions()
    {
        return [
            'error' => [
                'class' => 'yii\web\ErrorAction',
            ],
            'captcha' => [
                'class' => 'yii\captcha\CaptchaAction',
                'fixedVerifyCode' => YII_ENV_TEST ? 'testme' : null,
            ],
        ];
    }

    /**
     * Displays homepage.
     *
     */
    public function actionIndex()
    {
        if(Yii::$app->user->isGuest){
            return $this->redirect('/site/login');
        }else{
            return $this->redirect(['/user/tasks/'.Yii::$app->user->id]);
        }
    }

    /**
     * Login action.
     *
     * @return Response|string
     */
    public function actionLogin()
    {
        if (!Yii::$app->user->isGuest) {
            return $this->goHome();
        }

        $model = new LoginForm();
        if ($model->load(Yii::$app->request->post()) && $model->login()) {
            return $this->goBack();
        }

        $model->password = '';
        return $this->render('login', [
            'model' => $model,
        ]);
    }

    /**
     * Logout action.
     *
     * @return Response
     */
    public function actionLogout()
    {
        Yii::$app->user->logout();

        return $this->goHome();
    }

    public function actionSignout()
    {
        Yii::$app->user->logout();

        return $this->goHome();
    }

